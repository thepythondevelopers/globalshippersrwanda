                <p id="isPasted">Wondering what dimensional (DIM) weight is and how it factors into your international shipping costs?</p><h3><span style="text-decoration: underline;"><em><strong>Here's an easy-to-understand explanation.</strong></em></span></h3><p>DIM weight, or dimensional weight, refers to a billing technique that takes into account the package's size rather than its actual or "<strong>gross</strong>" weight. When determining the cost of an international shipment, carriers charge based on whichever is greater: <em>the actual weight or the DIM weight</em>. This is done to account for the space a package will occupy during transit relative to its weight.</p><p>As a practical example, let's consider a <em>2lb pillow</em> with large dimensions such as <em>18x30x4 inches</em>. Given its size, a carrier may bill the shipment as if it weighs <em>16 lbs</em>. It's important to note, however, that the DIM weight calculations vary across different carriers.</p><h4><strong>The Dimensional Weight (L x W x H / 139) is greater than the Actual Weight of the package and will be used for rates calculations.</strong></h4><p><strong><img data-fr-image-pasted="true" alt="Shipping Dimensions and Weight | UPS - United States" height="199" style="width: 354px; height: auto;" src="https://www.ups.com/assets/resources/webcontent/images/shipment-size-weight-measure-package-B-1224353-Q421.jpg" width="354" class="fr-fic fr-dii"></strong></p><p>To help you better understand and estimate your shipping costs, here are dimensional weight calculators provided by FedEx and DHL:</p><ul><li><a href="https://page.message.fedex.com/weight_calculator" rel="noopener noreferrer" target="_blank">FedEx Dimensional Weight Calculator</a></li><li><a href="https://www.dhl.com/us-en/home/ecommerce-solutions/business-help-center/chargeable-weight.html#:~:text=Calculate%20dimensional%20weight%20by%20multiplying,dimensional%20weight%20of%20your%20package&amp;text=Compare%20this%20number%20with%20